package org.example;

public class Calc {
    public static int som(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static double minus(double a, double b) {
        return a - b;
    }

    public static double som(double a, double b) {
        return a + b;
    }
}
